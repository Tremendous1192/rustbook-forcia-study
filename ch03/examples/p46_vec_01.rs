fn main() {
    // Create a vector v1 with elements 1, 2, 3, 4, 5
    let v1 = vec![1, 2, 3, 4, 5];

    // Create a vector v2 with five elements, all initialized to 0
    let v2 = vec![0; 5];

    // Print the contents of vector v1
    println!("{:?}", v1);

    // Print the contents of vector v2
    println!("{:?}", v2);
}
