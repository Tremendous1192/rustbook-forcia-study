fn main() {
    // タプル変数tを宣言し、(1, "2")という値で初期化します。
    let mut t = (1, "2");

    // タプルtの内容をデバッグフォーマットで出力します。初期値が表示されます。
    println!("{:?}", t);

    // タプルtの最初の要素（インデックス0）に新しい値2を代入します。
    t.0 = 2;

    // タプルtの2番目の要素（インデックス1）に新しい文字列"3"を代入します。
    t.1 = "3";

    // 更新後のタプルtの内容をデバッグフォーマットで再度出力します。
    println!("{:?}", t);
}
